/**
 * Class implements Kebab.
 */
public class Kebab {
    private int kebabId;

    /**
     * Construcor of Kebab Class.
     * @param kebab id of Kebab.
     */
    Kebab(int kebab)
    {
        this.kebabId = kebab;
    }

    /**
     * Method to get id of Kebab.
     * @return id of kebab.
     */
    public int getKebabId(){
        return this.kebabId;
    }
}
